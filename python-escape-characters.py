# SINGLE QUOTE
singleQuotes = 'It\'s alright'
print(singleQuotes)

# BACKSLASH
backSlash = "This will insert one \\ (backslash)"
print(backSlash)

# NEW LINE
newLine = "New \nLine"
print(newLine)

# CARRIAGE RETURN
carriageReturn = "Hello \rWorld"
print(carriageReturn)

# TAB
addSpace = "Add\tspace"
print(addSpace)

# BACKSPACE
backspace = "Remove \bspace"
print(backspace)

# OCTAL VALUE
octalValue = "\110\145\154\154\157"
print(octalValue) 

# HEX VALUE
hexValue = "\x48\x65\x6c\x6c\x6f"
print(hexValue)
